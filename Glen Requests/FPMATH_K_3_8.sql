[ID] = STU.ID,
[Name] = STU.LN + ', ' + STU.FN,
School = CASE STU.SC
WHEN 2 THEN 'El Toro'
WHEN 6 THEN 'Los Paseos'
WHEN 8 THEN 'Nordstrom'
WHEN 9 THEN 'Paradise'
WHEN 10 THEN 'SMG'
WHEN 11 THEN 'Walsh'
