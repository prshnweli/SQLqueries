SELECT 
STU.ID,
STU.FN, 
STU.LN,
STU.GR,
ATT.*
FROM ATT
JOIN STU ON (STU.SC = ATT.SC AND STU.SN = ATT.SN)
WHERE STU.DEL = 0
AND STU.TG = ''
AND STU.SC = 30

SELECT 
STU.ID, 
STU.FN, 
STU.LN, 
STU.GR, 
GRD.PD,
GRD.M2,
GRD.M4,
GRD.M6,
GRD.M8,
GRD.M10,
GRD.M12,
GRD.AB,
GRD.TD
FROM STU
JOIN GRD ON (STU.SC = GRD.SC AND STU.SN = GRD.SN)
WHERE STU.SC = 30